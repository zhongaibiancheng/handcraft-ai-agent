def get_role(role_key: str) -> str:
    """根据角色名称获取对应的 System Prompt"""
    if role_key not in ROLES:
        available = ", ".join(ROLES.keys())
        raise ValueError(
            f"角色 '{role_key}' 不存在。可用角色：{available}"
        )
    return ROLES[role_key]["prompt"]


def list_roles() -> None:
    """打印所有可用角色"""
    print("📋 可用的 System Prompt 角色预设：\n")
    for key, info in ROLES.items():
        tags = " | ".join(info["tags"])
        print(f"  {info['name']}")
        print(f"    关键字: {key}")
        print(f"    标签: {tags}")
        preview = info["prompt"][:50] + "..."
        print(f"    预览: {preview}\n")


if __name__ == "__main__":
    list_roles()
